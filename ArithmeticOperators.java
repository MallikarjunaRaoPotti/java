public class ArithmeticOperators {
    public static void main(String[] args){
        int a=10;
        int b=20;
        int sum=a+b;
        System.out.println(sum);
        int num1=50;
        int num2=40;
        int sub=num1-num2;
        System.out.println(sub);
        int num3=20;
        int num4=77;
        int mul=num3*num4;
        System.out.println(mul);
        float div=b/a;
        System.out.println(div);
        int rem=num1%num2;
        System.out.println(rem);

    }
    
}
